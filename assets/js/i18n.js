  /* ── ENGINE ── */
  var SKIP_TAGS = { SCRIPT: 1, STYLE: 1, NOSCRIPT: 1, IFRAME: 1, OBJECT: 1 };

  function walkTextNodes(node, fn) {
    if (node.nodeType === 3) {
      fn(node);
    } else if (node.nodeType === 1 && !SKIP_TAGS[node.tagName]) {
      for (var i = 0; i < node.childNodes.length; i++) {
        walkTextNodes(node.childNodes[i], fn);
      }
    }
  }

  function applyEN() {
    walkTextNodes(document.body, function (node) {
      var orig = node.nodeValue;
      var key = orig.trim();
      if (key && EN[key] !== undefined) {
        node.nodeValue = orig.replace(key, EN[key]);
      }
    });

    // Placeholders
    document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(function (el) {
      var key = el.placeholder;
      if (PLACEHOLDERS[key]) el.placeholder = PLACEHOLDERS[key];
    });

    // lang attribute & button label
    document.documentElement.lang = 'en';
    var label = document.getElementById('translate-label');
    if (label) label.textContent = 'PT';
  }

  /* ── API PÚBLICA ── */
  window.g6Translate = function () {
    if (localStorage.getItem('g6lang') === 'en') {
      localStorage.removeItem('g6lang');
      location.reload();
    } else {
      localStorage.setItem('g6lang', 'en');
      applyEN();
    }
  };

  /* ── AUTO-APLICAR AO CARREGAR ── */
  function init() {
    var label = document.getElementById('translate-label');
    if (localStorage.getItem('g6lang') === 'en') {
      applyEN();
    } else if (label) {
      label.textContent = 'EN';
    }
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

